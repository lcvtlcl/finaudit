// metrics1: исключение внутренних переводов/займов из выручки.
//
// Идея: перевод между своими счетами, взнос учредителя, выдача/возврат займа —
// это не выручка и не операционный расход бизнеса, хотя деньги реально
// двигались по счёту. Детект по ключевым словам в назначении платежа —
// детерминированно, без ИИ (следуя общей рамке проекта).
package metrics

import "strings"

// internalTransferKeywords — фразы в НазначениеПлатежа/Purpose, указывающие
// на внутренний перевод или заём (не выручка/не операционный расход).
var internalTransferKeywords = []string{
	"перевод между своими счетами",
	"перевод собственных средств",
	"перевод на собственный счет",
	"перевод на свой счет",
	"внутренний перевод",
	"взнос учредителя",
	"вклад учредителя",
	"пополнение счета учредителем",
	"предоставление займа",
	"выдача займа",
	"возврат займа",
	"погашение займа",
	"по договору займа",
	"по договору беспроцентного займа",
}

// isInternalTransferOrLoan определяет, что платёж — внутреннее движение
// денег (между своими счетами / заём учредителя), а не выручка/расход.
func isInternalTransferOrLoan(purpose string) bool {
	p := strings.ToLower(purpose)
	for _, kw := range internalTransferKeywords {
		if strings.Contains(p, kw) {
			return true
		}
	}
	return false
}
