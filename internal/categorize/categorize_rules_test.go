package categorize

import (
	"testing"

	"github.com/wwpp/finaudit/internal/models"
)

// Проверяет расширенные детерминированные правила (сокращение «Прочее»).
func TestCategorize_ExtendedRules(t *testing.T) {
	cases := []struct {
		purpose string
		want    string
	}{
		{"Комиссия за обслуживание счёта", "Банковские комиссии"},
		{"Оплата за рекламу в Яндекс.Директ", "Маркетинг и реклама"},
		{"Услуги логистики и доставки груза", "Транспорт и логистика"},
		{"Продление лицензии на ПО и хостинг", "ПО и подписки"},
		{"Юридические услуги по договору", "Услуги и подряд"},
		{"Оплата за партию товара", "Закупки"},
		// пометка «В т.ч. НДС 20%» не должна утягивать обычный платёж в «Налоги»
		{"Арендная плата за офис за 01.2025. В т.ч. НДС 20%", "Аренда"},
		{"Крупная предоплата по договору поставки №999/2025 (срочный заказ). В т.ч. НДС 20%", "Закупки"},
		{"НДФЛ за 01.2025", "Налоги"},
		{"Уплата НДС/налога по требованию. Срок — сегодня", "Налоги"},
		{"Непонятное списание без ключевых слов", "Прочее"},
	}
	for _, c := range cases {
		got := Categorize([]models.Transaction{{Purpose: c.purpose, Direction: models.Out}})
		if got[0].Category != c.want {
			t.Errorf("purpose %q: got %q, want %q", c.purpose, got[0].Category, c.want)
		}
	}
}
